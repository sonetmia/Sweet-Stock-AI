const OPENROUTER_API_KEY = process.env.OPENROUTER_API_KEY;
const API_URL = "https://openrouter.ai/api/v1/chat/completions";
const MODEL = "google/gemini-2.0-flash-lite-preview-02-05:free"; // বা যেকোনো ফ্রি/পেইড মডেল

export async function POST(request) {
  try {
    const { messages, customMemories } = await request.json();

    // ব্যক্তিগত মেমোরি যুক্ত করে সিস্টেম প্রম্পট সম্প্রসারণ
    let fullSystemPrompt = COURSE_SYSTEM_PROMPT;
    if (customMemories && customMemories.length > 0) {
      fullSystemPrompt +=
        "\n\nতোমার কাছে নিচের ইউজারের ব্যক্তিগত মেমোরি রয়েছে:\n" +
        customMemories.map((m, i) => `${i + 1}. ${m}`).join("\n");
    }

    const systemMessage = { role: "system", content: fullSystemPrompt };

    // API-তে পাঠানোর মেসেজ
    const apiMessages = [systemMessage, ...messages];

    const payload = {
      model: MODEL,
      messages: apiMessages,
      temperature: 0.7,
    };

    const response = await fetch(API_URL, {
      method: "POST",
      headers: {
        Authorization: `Bearer ${OPENROUTER_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify(payload),
    });

    if (!response.ok) {
      const err = await response.json();
      return NextResponse.json(
        { error: err.error?.message || "API error" },
        { status: response.status }
      );
    }

    const data = await response.json();
    const reply = data.choices[0].message.content;

    return NextResponse.json({ reply });
  } catch (error) {
    return NextResponse.json(
      { error: error.message || "Internal server error" },
      { status: 500 }
    );
  }
}